let mongoose = require("mongoose");
let francophoneMobilityMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let francophoneMobilityMetaSection = mongoose.model("Francophone Mobility  Meta", francophoneMobilityMetaSchema);
module.exports = francophoneMobilityMetaSection;
