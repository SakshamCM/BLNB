let mongoose = require("mongoose");
let prioritiesProgramMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let prioritiesProgramMetaSection = mongoose.model( "priorities Program Meta", prioritiesProgramMetaSchema);
module.exports = prioritiesProgramMetaSection;
