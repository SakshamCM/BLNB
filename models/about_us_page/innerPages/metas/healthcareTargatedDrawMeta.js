let mongoose = require("mongoose");
let healthcareTargatedDrawMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let healthcareTargatedDrawMetaSection = mongoose.model("Healthcare Targated Draw Meta", healthcareTargatedDrawMetaSchema);
module.exports = healthcareTargatedDrawMetaSection;
