let mongoose = require("mongoose");
let canadianExperienceClassMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let canadianExperienceClassMetaSection = mongoose.model("Canadian Experience Class Meta", canadianExperienceClassMetaSchema);
module.exports = canadianExperienceClassMetaSection;
