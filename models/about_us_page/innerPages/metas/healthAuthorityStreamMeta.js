let mongoose = require("mongoose");
let healthAuthorityStreamMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let healthAuthorityStreamMetaSection = mongoose.model("Health Authority Stream Meta", healthAuthorityStreamMetaSchema);
module.exports = healthAuthorityStreamMetaSection;
