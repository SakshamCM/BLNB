let mongoose = require("mongoose");
let bridgingOpenWorkMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let bridgingOpenWorkMetaSection = mongoose.model("Bridging Open Work Meta", bridgingOpenWorkMetaSchema);
module.exports = bridgingOpenWorkMetaSection;
