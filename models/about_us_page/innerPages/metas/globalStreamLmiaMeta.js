let mongoose = require("mongoose");
let globalStreamLmiaMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let globalStreamLmiaMetaSection = mongoose.model("Global Stream Lmia Meta", globalStreamLmiaMetaSchema);
module.exports = globalStreamLmiaMetaSection;
