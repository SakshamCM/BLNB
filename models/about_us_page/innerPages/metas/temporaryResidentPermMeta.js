let mongoose = require("mongoose");
let temporaryResidentPermMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let temporaryResidentPermMetaSection = mongoose.model( "temporary Resident Permit Meta", temporaryResidentPermMetaSchema);
module.exports = temporaryResidentPermMetaSection;
