let mongoose = require("mongoose");
let lonelyCanadianMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let lonelyCanadianMetaSection = mongoose.model( "Lonely Canadian Meta", lonelyCanadianMetaSchema);
module.exports = lonelyCanadianMetaSection;
