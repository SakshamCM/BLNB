let mongoose = require("mongoose");
let agriFoodPilotProgMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let agriFoodPilotProgMetaSection = mongoose.model("Agri Food Pilot Program Meta", agriFoodPilotProgMetaSchema);
module.exports = agriFoodPilotProgMetaSection;
