let mongoose = require("mongoose");
let temporaryResidencyMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgTitle: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaOgDesc: {
      type: String,
      default: "Brightlight Immigration",
    },
    metaKeywords: {
      type: String,
      default: "Brightlight Immigration",
    }
  },
  {
    timestamps: true,
  }
);

let temporaryResidencyMetaSection = mongoose.model( "temporary Residency Meta", temporaryResidencyMetaSchema);
module.exports = temporaryResidencyMetaSection;
