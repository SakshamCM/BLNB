let mongoose = require("mongoose");
let visitorMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let visitorMetaSection = mongoose.model("Visitor Visa Meta", visitorMetaSchema);
module.exports = visitorMetaSection;
