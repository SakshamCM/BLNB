let mongoose = require("mongoose");
let transportMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let transportMetaSection = mongoose.model("Transport Occupation Meta", transportMetaSchema);
module.exports = transportMetaSection;
