let mongoose = require("mongoose");
let permanentMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let permanentMetaSection = mongoose.model("Permanent Residency Meta", permanentMetaSchema);
module.exports = permanentMetaSection;
