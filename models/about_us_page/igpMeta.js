let mongoose = require("mongoose");
let igpMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let igpMetaSection = mongoose.model("IGP Meta", igpMetaSchema);
module.exports = igpMetaSection;
