let mongoose = require("mongoose");
let bpMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let bpMetaSection = mongoose.model("BC-PNP Page Meta", bpMetaSchema);
module.exports = bpMetaSection;
