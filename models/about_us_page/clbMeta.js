let mongoose = require("mongoose");
let clbMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let clbMetaSection = mongoose.model("CLB Meta", clbMetaSchema);
module.exports = clbMetaSection;
