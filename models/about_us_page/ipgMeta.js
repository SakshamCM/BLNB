let mongoose = require("mongoose");
let ipgMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let ipgMetaSection = mongoose.model("IPG Meta", ipgMetaSchema);
module.exports = ipgMetaSection;
