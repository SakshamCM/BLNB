let mongoose = require("mongoose");
let pflMetaSchema = mongoose.Schema(
  {
    metaTitle: {
      type: String,
    },
    metaDesc: {
      type: String,
    },
    metaOgTitle: {
      type: String,
    },
    metaOgDesc: {
      type: String,
    },
    metaKeywords: {
      type: String
    }
  },
  {
    timestamps: true,
  }
);

let pflMetaSection = mongoose.model("PFL Meta", pflMetaSchema);
module.exports = pflMetaSection;
